                    saved from the righteous judgment of God, a person must repent to God and believe in the 
                    Lord Jesus Christ (Acts 2:38; 16:31; 26:20; John 3:15-16) to be forgiven of his or her sins 
                    and to be redeemed, justified, and regenerated (Acts 10:43; Rom. 3:24; Acts 13:39; John 3:6). 
                    Through regeneration we become the children of God (John 1:12) and members of Christ 
                    (1 Cor. 12:27). It is our great priviledge as co-laborers with God to preach this gospel to all peoples.
                </p>
